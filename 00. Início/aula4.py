# Tipos int e float
# int --> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# Positivo.
print(11) #int
print(-11) #int
print(0)

# float -> Número com ponto flutuante 
# o tipo float representa qualque número
# positivo ou negativo com ponto flutuante
# float sem sinal é positivo
print(1.1, 10.11, sep='/')
print(0.0, -1.15, sep='/')

# A funçaoo type mostra o tipo que o Python
# inferiu ao valor
print(type(''))
print(type(3))
print(type(-4))
print(type(3.54))